	public MaterialColor(Color c)
	{
		super(c.getRed(), c.getGreen(), c.getBlue());
	}

	public MaterialColor bleach (MaterialColor MaterialColor, float amount) {
		int red = (int) ((MaterialColor.getRed () * (1 - amount) / 255 + amount) * 255);
		int green = (int) ((MaterialColor.getGreen () * (1 - amount) / 255 + amount) * 255);
		int blue = (int) ((MaterialColor.getBlue () * (1 - amount) / 255 + amount) * 255);
		return new MaterialColor (red, green, blue);
	}

	public String toHex() {
		return String.format("#%02x%02x%02x", getRed(), getGreen(), getBlue());
	}
